-- 4) Применить: company.segment <- segment_map ----------------
-- На каждую компанию берём самый длинный (самый специфичный) подошедший
-- паттерн, чтобы исключить неоднозначность при пересечении шаблонов.
UPDATE finance.company c
SET segment = sub.segment
FROM (
    SELECT DISTINCT ON (comp_id) comp_id, segment
    FROM (
        SELECT c.id AS comp_id, m.segment, length(m.pattern) AS plen
        FROM finance.company c
        JOIN finance.segment_map m ON c.short_name ILIKE m.pattern
    ) j
    ORDER BY comp_id, plen DESC
) sub
WHERE c.id = sub.comp_id;

-- 5) Байт-точная уникальность паттернов (после сида/apply, чтобы они
--    гарантированно отработали; индекс не зависит от коллации БД) -------
CREATE UNIQUE INDEX IF NOT EXISTS uq_segment_map_pattern
    ON finance.segment_map (pattern COLLATE "C");

-- 6) Проверка — должно вернуть 0 непокрытых --------------------
SELECT short_name FROM finance.company WHERE segment IS NULL ORDER BY short_name;

-- 7) Сводка по сегментам --------------------------------------
SELECT segment, COUNT(*) AS companies
FROM finance.company
GROUP BY segment
ORDER BY companies DESC, segment;
