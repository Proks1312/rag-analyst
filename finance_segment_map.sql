--     и НЕ дропает таблицу.
--
-- Про коллацию: дефолтная коллация БД склеивает близкие строки (например
-- ё=е, _=пробел) при сравнении ключей, из-за чего обычный PRIMARY KEY на
-- pattern роняет сид с «ON CONFLICT ... cannot affect row a second time».
-- Поэтому уникальность вынесена в индекс с COLLATE "C" (байт-точный), а
-- сама колонка остаётся в дефолтной коллации — чтобы ILIKE по-прежнему
-- корректно сворачивал регистр кириллицы при матчинге имён.
--
-- Про pattern:
--   • это ILIKE-шаблон по company.short_name — терпит регистр, пробел и
--     подчёркивание, можно несколько частей: '%ул%полимер%композит%';
--   • при совпадении НЕСКОЛЬКИХ паттернов выигрывает САМЫЙ ДЛИННЫЙ
--     (самый специфичный) — см. apply-UPDATE в секции 4.
-- ============================================================

-- 1) Колонка segment в company (на случай чистой БД) ----------
ALTER TABLE finance.company ADD COLUMN IF NOT EXISTS segment TEXT;
CREATE INDEX IF NOT EXISTS idx_company_segment ON finance.company(segment);
COMMENT ON COLUMN finance.company.segment IS
    'Отраслевой сегмент. Проставляется из finance.segment_map (загрузчиком или секцией 4 этого файла).';

-- 2) Таблица-справочник (пересоздаём — сид идемпотентен и не зависит
--    от коллации БД; уникальность в отдельном индексе с COLLATE "C") ----
DROP TABLE IF EXISTS finance.segment_map;
CREATE TABLE finance.segment_map (
    pattern  TEXT NOT NULL,
    segment  TEXT NOT NULL
);
COMMENT ON TABLE finance.segment_map IS
    'Маппинг «ILIKE-паттерн имени → сегмент». Источник истины для company.segment.';

